    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
        if(txtNama.getText().equals("") || txtNpm.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Data Tidak Boleh Kosong", null, JOptionPane.ERROR_MESSAGE);
            txtNama.requestFocus();
            hapuslayar();
        }else{
            st = con.createStatement();
            String sql = ("SELECT * FROM tb_mhs WHERE npm = '"+txtNpm.getText()+"' AND nama = '"+txtNama.getText()+"'");
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                User.setRole(rs.getString("role"));
                
                System.out.println(User.getRole());
                if(User.getRole().equals("1")) {
                    this.dispose();
                    new Dashboard().setVisible(true);
                } else if(User.getRole().equals("2")) {
                    this.dispose();
                    new Dashboard2().setVisible(true);
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Nama dan NPM Salah", "Pesan",
                JOptionPane.ERROR_MESSAGE);
                hapuslayar();
            }
        }
    }catch(Exception e){
        e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

